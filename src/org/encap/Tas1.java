package org.encap;

public class Tas1 {
	private  int id;
	private  short age;
	private String name;
	public Tas1(int id,short aged,String name) {
		super();
		this.id=id;
		this.age=aged;
		this.name=name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public short getAge() {
		return age;
	}
	public void setAge(short age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
		

}
